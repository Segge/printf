#include <stdio.h>

#include <stdlib.h>

#include <time.h>

/**
 *Description: main-prints alphabet in lowercase and then in uppercase.
 *Return: 0 if success
 */

int main(void)
{
char lowercase = 'a';

while (lowercase <= 'z')
{
putchar(lowercase);
lowercase++;
}
lowercase = 'A';

while (lowercase <= 'Z')
{
putchar(lowercase);
lowercase++;
}
putchar('\n');
return (0);
}
